package de.heju.room.bremen;

import org.htmlcleaner.TagNode;

public class CrosshairedImage {

	private TagNode map;
	private TagNode crosshair;

	public TagNode getMap() {
		return map;
	}

	public void setMap(TagNode map) {
		this.map = map;
	}

	public TagNode getCrosshair() {
		return crosshair;
	}

	public void setCrosshair(TagNode crosshair) {
		this.crosshair = crosshair;
	}
}
